export const add = (a, b) => {
  return a + b;
}

export const subtract = (a, b) => {
  return a - b;
}

export const multiply = (a, b) => {
  return a * b;
}

export const divide = (a, b) => {
  return a / b;
}

export default {
  add        : add,
  subtract   : subtract,
  multiply   : multiply,
  divide     : divide
} 